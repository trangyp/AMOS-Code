"""AMOS Cognitive Repair System - Production Integration

Uses:
1. AMOS Brain for reasoning about code issues
2. Model Fabric (LiteLLM + local LLMs) for code generation
3. Repo Doctor for security verification
4. Thinking Engine for state transformation

This is REAL integration - not stubs or fake features.
"""

import asyncio
import logging
import subprocess
from dataclasses import dataclass, field
from datetime import UTC, datetime, timezone

UTC = UTC
from pathlib import Path

# AMOS Brain imports - REAL
from amos_brain import BrainClient
from amos_brain.state_manager import ReasoningStep, WorkflowSession, get_state_manager
from amos_brain.thinking_engine import ThinkingEngine

# Model Fabric imports - REAL
from amos_model_fabric.litellm_setup import LiteLLMSetup
from amos_model_fabric.providers import OllamaProvider
from amos_model_fabric.schemas import FabricRequest

# Repo Doctor imports - REAL
from repo_doctor.security_scanner import (
    SecurityVerificationEngine,
    VerificationReceipt,
)

logger = logging.getLogger(__name__)


@dataclass
class RepairPlan:
    """Cognitive repair plan generated by brain."""

    plan_id: str
    issue_description: str
    root_cause: str
    repair_strategy: str
    affected_files: list[str]
    estimated_risk: str
    confidence: float
    reasoning_chain: list[str]
    law_compliant: bool
    created_at: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())


@dataclass
class GeneratedRepair:
    """Actual code repair with verification status."""

    repair_id: str
    plan_id: str
    file_path: str
    original_code: str
    repaired_code: str
    changes_description: str
    model_used: str
    generation_time_ms: float
    verification_receipt: Optional[VerificationReceipt] = None
    status: str = "pending"  # pending, verified, rejected, applied


@dataclass
class CognitiveRepairResult:
    """Complete result of cognitive repair process."""

    issue: str
    plan: Optional[RepairPlan]
    repairs: list[GeneratedRepair]
    thinking_steps: int
    security_passed: bool
    repairs_applied: int
    total_time_ms: float
    session_id: str


class AMOSCognitiveRepair:
    """
    Production cognitive code repair system.

    Architecture:
        1. Brain analyzes issue → RepairPlan
        2. LiteLLM generates repairs → GeneratedRepair
        3. Repo Doctor verifies → VerificationReceipt
        4. Brain decides → apply/reject

    Usage:
        repair = AMOSCognitiveRepair()
        result = await repair.fix_code_issue(
            "Fix SQL injection in user login",
            files=["src/auth.py"]
        )
    """

    def __init__(self, repo_path: Optional[Path] = None):
        self.repo_path = repo_path or Path.cwd()
        self.brain = BrainClient(repo_path=str(self.repo_path))
        self.thinking = ThinkingEngine()
        self.security = SecurityVerificationEngine()
        self.state = get_state_manager()
        self.litellm = LiteLLMSetup()

        # Ollama provider for code generation
        self.ollama = OllamaProvider(base_url="http://localhost:11434")

        self._session: Optional[WorkflowSession] = None

    async def initialize(self) -> bool:
        """Initialize all subsystems."""
        # Check Ollama
        if not await self.ollama.health_check():
            logger.error("Ollama not available at localhost:11434")
            return False

        logger.info("✓ Ollama connected")

        # Check security scanners
        scanners_ok = self._check_scanners()
        if not scanners_ok:
            logger.warning("Some security scanners not available")

        return True

    def _check_scanners(self) -> bool:
        """Check if security scanners are installed."""
        scanners = ["semgrep", "trivy", "gitleaks", "ruff"]
        available = []
        for scanner in scanners:
            result = subprocess.run(
                ["which", scanner],
                capture_output=True,
            )
            if result.returncode == 0:
                available.append(scanner)

        logger.info(f"Security scanners available: {available}")
        return len(available) >= 2  # Need at least 2

    async def fix_code_issue(
        self,
        issue_description: str,
        files: Optional[list[str]] = None,
        domain: str = "software",
    ) -> CognitiveRepairResult:
        """
        End-to-end cognitive code repair.

        1. Brain thinks about the issue
        2. Generate repair plan
        3. Generate code repairs using LLM
        4. Verify with security scanners
        5. Brain decides to apply or reject
        """
        start_time = datetime.now(timezone.utc)
        session_id = self.state.start_session(
            goal=f"Fix: {issue_description}",
            domain=domain,
        )

        # Step 1: Brain thinks about the issue
        logger.info("Step 1: Brain analyzing issue...")
        plan = await self._create_repair_plan(issue_description, files, session_id)

        if not plan or not plan.law_compliant:
            return CognitiveRepairResult(
                issue=issue_description,
                plan=plan,
                repairs=[],
                thinking_steps=0,
                security_passed=False,
                repairs_applied=0,
                total_time_ms=0,
                session_id=session_id,
            )

        # Step 2: Generate repairs using LLM
        logger.info("Step 2: Generating repairs with local LLM...")
        repairs = await self._generate_repairs(plan, files or plan.affected_files)

        # Step 3: Verify with security scanners
        logger.info("Step 3: Running security verification...")
        verified_repairs = await self._verify_repairs(repairs)

        # Step 4: Brain decides which repairs to apply
        logger.info("Step 4: Brain deciding on repairs...")
        applied_count = await self._apply_approved_repairs(verified_repairs, session_id)

        # Calculate timing
        end_time = datetime.now(timezone.utc)
        total_ms = (end_time - start_time).total_seconds() * 1000

        # Get thinking step count
        session = self.state._active_sessions.get(session_id)
        thinking_steps = len(session.reasoning_chain) if session else 0

        return CognitiveRepairResult(
            issue=issue_description,
            plan=plan,
            repairs=verified_repairs,
            thinking_steps=thinking_steps,
            security_passed=all(
                r.verification_receipt and r.verification_receipt.overall_passed
                for r in verified_repairs
            ),
            repairs_applied=applied_count,
            total_time_ms=total_ms,
            session_id=session_id,
        )

    async def _create_repair_plan(
        self,
        issue: str,
        files: Optional[list[str]],
        session_id: str,
    ) -> Optional[RepairPlan]:
        """Use brain to create repair plan."""
        import uuid

        # Brain thinks about the issue
        brain_response = await self.brain.think(
            query=f"Analyze this code issue and create repair plan: {issue}\n"
            f"Affected files: {files or 'unknown'}",
            domain="software",
            require_law_compliance=True,
        )

        if not brain_response.success:
            logger.error(f"Brain failed to analyze: {brain_response.violations}")
            return None

        # Record reasoning step
        step = ReasoningStep(
            step_id=f"step-{uuid.uuid4().hex[:8]}",
            timestamp=datetime.now(timezone.utc).isoformat(),
            description=f"Analyzed issue: {issue[:50]}...",
            perspective="repair_planning",
            law_compliance={"L1": True, "L2": True, "L3": True},
            kernel_activations=["analysis", "planning"],
            input_context={"issue": issue, "files": files},
            output_result=brain_response.content,
            confidence=brain_response.confidence,
        )

        session = self.state._active_sessions.get(session_id)
        if session:
            session.add_step(step)

        # Parse brain response into structured plan
        return RepairPlan(
            plan_id=f"plan-{uuid.uuid4().hex[:8]}",
            issue_description=issue,
            root_cause=self._extract_root_cause(brain_response.content),
            repair_strategy=self._extract_strategy(brain_response.content),
            affected_files=files or self._extract_files(brain_response.content),
            estimated_risk=brain_response.confidence,
            confidence=0.8 if brain_response.confidence == "high" else 0.6,
            reasoning_chain=brain_response.reasoning,
            law_compliant=brain_response.law_compliant,
        )

    async def _generate_repairs(
        self,
        plan: RepairPlan,
        files: list[str],
    ) -> list[GeneratedRepair]:
        """Generate code repairs using local LLM via Ollama."""
        import uuid

        repairs = []

        for file_path in files:
            target_file = self.repo_path / file_path
            if not target_file.exists():
                logger.warning(f"File not found: {file_path}")
                continue

            original_code = target_file.read_text()

            # Build repair prompt
            prompt = self._build_repair_prompt(
                plan.issue_description,
                plan.repair_strategy,
                file_path,
                original_code,
            )

            # Generate with Ollama
            gen_start = datetime.now(timezone.utc)

            try:
                request = FabricRequest(
                    messages=[{"role": "user", "content": prompt}],
                    model="qwen2.5-coder:14b",
                    temperature=0.3,
                    max_tokens=4096,
                )

                response = await self.ollama.complete(request)
                repaired_code = response.content

                gen_end = datetime.now(timezone.utc)
                gen_time = (gen_end - gen_start).total_seconds() * 1000

                repair = GeneratedRepair(
                    repair_id=f"repair-{uuid.uuid4().hex[:8]}",
                    plan_id=plan.plan_id,
                    file_path=file_path,
                    original_code=original_code,
                    repaired_code=repaired_code,
                    changes_description=self._describe_changes(original_code, repaired_code),
                    model_used=response.model,
                    generation_time_ms=gen_time,
                )

                repairs.append(repair)
                logger.info(f"Generated repair for {file_path} in {gen_time:.0f}ms")

            except Exception as e:
                logger.error(f"Failed to generate repair for {file_path}: {e}")

        return repairs

    async def _verify_repairs(self, repairs: list[GeneratedRepair]) -> list[GeneratedRepair]:
        """Verify repairs using security scanners."""
        import shutil
        import tempfile

        verified = []

        for repair in repairs:
            # Create temp copy with repair applied
            with tempfile.TemporaryDirectory() as temp_dir:
                temp_repo = Path(temp_dir) / "repo"
                shutil.copytree(self.repo_path, temp_repo, ignore=lambda x, y: [".git"])

                # Apply repair
                target = temp_repo / repair.file_path
                if target.exists():
                    target.write_text(repair.repaired_code)

                # Run security verification
                receipt = await self.security.verify(
                    repo_path=temp_repo,
                    receipt_id=repair.repair_id,
                )

                repair.verification_receipt = receipt

                # Determine status
                if receipt.critical_count > 0:
                    repair.status = "rejected"
                    logger.warning(f"Repair {repair.repair_id} REJECTED - critical issues found")
                elif receipt.high_count > 0:
                    repair.status = "needs_review"
                    logger.info(f"Repair {repair.repair_id} needs review")
                else:
                    repair.status = "verified"
                    logger.info(f"Repair {repair.repair_id} VERIFIED")

                verified.append(repair)

        return verified

    async def _apply_approved_repairs(
        self,
        repairs: list[GeneratedRepair],
        session_id: str,
    ) -> int:
        """Brain decides and applies approved repairs."""
        applied = 0

        for repair in repairs:
            if repair.status != "verified":
                continue

            # Brain decides
            decision = await self.brain.decide(
                f"Apply repair to {repair.file_path}? Changes: {repair.changes_description}",
                options=["apply", "reject", "modify"],
            )

            if decision.approved:
                target = self.repo_path / repair.file_path
                target.write_text(repair.repaired_code)
                repair.status = "applied"
                applied += 1
                logger.info(f"Applied repair to {repair.file_path}")

                # Record step
                step = ReasoningStep(
                    step_id=f"apply-{repair.repair_id}",
                    timestamp=datetime.now(timezone.utc).isoformat(),
                    description=f"Applied repair to {repair.file_path}",
                    perspective="execution",
                    law_compliance={"L1": True, "L2": True, "L3": True},
                    kernel_activations=["execution", "validation"],
                    input_context={"repair_id": repair.repair_id},
                    output_result="success",
                    confidence="high",
                )

                session = self.state._active_sessions.get(session_id)
                if session:
                    session.add_step(step)

        return applied

    def _build_repair_prompt(
        self,
        issue: str,
        strategy: str,
        file_path: str,
        code: str,
    ) -> str:
        """Build prompt for LLM code repair."""
        return f"""You are an expert software engineer. Fix the following issue:

ISSUE: {issue}

STRATEGY: {strategy}

FILE: {file_path}

CURRENT CODE:
```
{code}
```

Provide the COMPLETE fixed code. Do not explain, just output the fixed file content.
Ensure the fix:
1. Resolves the issue completely
2. Maintains existing functionality
3. Follows security best practices
4. Preserves code style

FIXED CODE:
"""

    def _extract_root_cause(self, brain_content: str) -> str:
        """Extract root cause from brain response."""
        lines = brain_content.split("\n")
        for line in lines:
            if "root cause" in line.lower() or "cause" in line.lower():
                return line.strip()
        return "unknown"

    def _extract_strategy(self, brain_content: str) -> str:
        """Extract repair strategy from brain response."""
        lines = brain_content.split("\n")
        for line in lines:
            if "strategy" in line.lower() or "approach" in line.lower():
                return line.strip()
        return "analyze and fix"

    def _extract_files(self, brain_content: str) -> list[str]:
        """Extract affected files from brain response."""
        # Simple extraction - look for file patterns
        import re

        files = re.findall(r"[\w/]+\.py", brain_content)
        return files[:5]  # Limit to 5 files

    def _describe_changes(self, original: str, repaired: str) -> str:
        """Describe changes between original and repaired code."""
        orig_lines = original.split("\n")
        rep_lines = repaired.split("\n")

        changed = sum(1 for a, b in zip(orig_lines, rep_lines) if a != b)
        added = len(rep_lines) - len(orig_lines)

        return f"{changed} lines modified, {added} lines added/removed"


async def demo():
    """Demonstrate cognitive repair system."""
    print("=" * 60)
    print("AMOS Cognitive Repair System - Demo")
    print("=" * 60)

    repair = AMOSCognitiveRepair()

    # Initialize
    if not await repair.initialize():
        print("\n❌ Failed to initialize. Ensure Ollama is running.")
        print("   Start Ollama: ollama serve")
        return

    # Create demo vulnerable file
    demo_file = Path("demo_vulnerable.py")
    demo_file.write_text("""
def login(username, password):
    # Vulnerable SQL injection
    query = "SELECT * FROM users WHERE username='" + username + "' AND password='" + password + "'"
    return query
""")

    print(f"\nCreated demo file: {demo_file}")
    print("Issue: SQL injection vulnerability")

    # Run cognitive repair
    print("\n" + "-" * 60)
    print("Running cognitive repair...")
    print("-" * 60)

    result = await repair.fix_code_issue(
        "Fix SQL injection vulnerability in login function",
        files=["demo_vulnerable.py"],
    )

    print("\n" + "=" * 60)
    print("RESULT:")
    print("=" * 60)
    print(f"Session ID: {result.session_id}")
    print(f"Thinking steps: {result.thinking_steps}")
    print(f"Repairs generated: {len(result.repairs)}")
    print(f"Repairs applied: {result.repairs_applied}")
    print(f"Security passed: {result.security_passed}")
    print(f"Total time: {result.total_time_ms:.0f}ms")

    if result.plan:
        print(f"\nRoot cause: {result.plan.root_cause}")
        print(f"Strategy: {result.plan.repair_strategy}")

    # Show final code
    if result.repairs_applied > 0:
        final_code = demo_file.read_text()
        print("\nFinal code:")
        print(final_code)

    # Cleanup
    demo_file.unlink(missing_ok=True)

    print("\n" + "=" * 60)
    print("Demo complete")
    print("=" * 60)


if __name__ == "__main__":
    asyncio.run(demo())
